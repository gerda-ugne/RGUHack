package gerda.arcfej.dreamrealm.map;

/**
 * Abstract class that adds position element
 * to the needed classes.
 */
public abstract class Position {

    public int x;
    public int y;

    public Position() {
        this.x = x;
        this.y = y;
    }

    public void setPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
